package org.example.Conditionalstts;

public class enumstt {
    public static void main(String[] args) {
        String output;


        int grade;
        grade = 60;
        switch (grade / 10) {
            case 10:
                ;
                System.out.println("output is" + "Excellent");
                break;
            case 9:
                ;
                System.out.println("output is" + "Wow");
                break;
            case 8:
                ;
                System.out.println("output is" + "very good");
                break;
            case 7:
                ;
                System.out.println("output is" + "good");
                break;
            case 6:
                ;
                System.out.println("output is" + "average");
                break;
            default:
                ;
                System.out.println("output is" + "fail");
                break;
        }
    }
}
