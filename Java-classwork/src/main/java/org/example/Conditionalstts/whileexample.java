package org.example.Conditionalstts;

public class whileexample {
    public static void main(String[] args) {
        int x=0;
        while(x<=10)
        {
            x=x+1;
            System.out.println("print \t"+x);
        }
        int y=10;

        do{
            y=y-1;
            System.out.println("print \t"+y);
        }while(y<=10);
    }
}
