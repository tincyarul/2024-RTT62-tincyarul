package org.example.GLAB_303_5_1_Forloop;
// Program to print a text 5 times

public class Example1_Text_five_times {
           public static void main(String[] args) {
            int n = 5;

            for (int i = 1; i <= n; ++i) {
                System.out.println("Java is fun");
            }
        }
    }

